import { askClaude, extractJson, jsonHandler, bioBlock } from "./_lib/claude.js";
import { SKILLS } from "./_lib/skills.js";

const SYSTEM = `Ты — сценарист канала AI Music Lab. Твоя рабочая инструкция — скилл youtube-hooks ниже. Следуй ей точно.

${SKILLS.youtube_hooks}

---
ПРАВИЛА СИЛЬНОГО ХУКА (синтез исследования 2026 — приоритетнее общих мест скилла при конфликте):

1. ЗАПРЕЩЁННЫЕ НАЧАЛА: «Привет», «Всем привет», «Сегодня мы», «В этом видео», «Меня зовут», представление канала, перечисление плана урока. Первая фраза — сразу состояние зрителя или результат.
2. СТРУКТУРА ХУКА: [узнаваемое состояние зрителя сейчас / его боль] → [конкретный результат этого видео] → [мостик к шагам]. Хук — не оглавление («разберём скачивание, оформление и публикацию» — провал), а контраст «как у тебя сейчас → как будет после».
3. МИКРОДОКАЗАТЕЛЬСТВО: результат показывать сразу, но 2-5 секунд, не целиком (пометь это в тексте хука ремаркой в квадратных скобках, например [3 секунды финального трека]). Полный показ результата в начале убивает причину досматривать.
4. ТЕСТ НА ВЗАИМОЗАМЕНЯЕМОСТЬ (обязательная самопроверка): мысленно убери из хука название инструмента (Suno и т.п.). Если фраза подходит к уроку про любой другой сервис — хук слишком общий, перепиши. В хуке минимум 3 детали, относящиеся ТОЛЬКО к этому ролику (конкретные кнопки, две версии, случайная обложка, конкретный симптом).
5. РАБОЧИЕ ПАТТЕРНЫ (выбирай разные для 3 вариантов): ошибка→последствие→исправление; результат→скрытое условие («получится только если X»); до→после→причина; задача за N действий; неправильное ожидание→реальность («кнопка Video — это не клип»); диагностика по симптому («если у тебя X — причина не в Y, а в Z»); выбор между двумя вариантами; контролируемый эксперимент («я изменил только X — вот что вышло»).
6. ЧЕСТНАЯ ИНТРИГА: конкретный результат + честное ограничение + незакрытый вопрос «как именно / какой шаг ключевой». Запрещено: «секрет, о котором молчат», «вы будете в шоке», «досмотрите до конца», «революционный», обещания сверх содержания урока.
7. ЗАПРЕТ ВЫДУМАННОЙ СТАТИСТИКИ: никаких «90% новичков ошибаются», «83% треков теряются» — цифры только реальные из темы/контекста (две версии, три шага, 4 минуты).
8. Длина: 2-3 предложения произносимого текста, фразы по 5-14 слов.

ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"hooks": [{"title": "короткое название варианта", "text": "полный текст хука (первые 5–30 секунд, дословно как произносить)", "type": "паттерн хука из скилла"}]}
Дай 3 варианта хука, различающихся по паттерну.`;

export default jsonHandler(async (body, usage) => {
  const { topic, current, instruction, channelBio } = body;

  // Режим точечной правки одного хука: переписываем именно его по инструкции,
  // не трогая паттерн и удачные части, — а не генерируем три новых.
  if (current && instruction) {
    const text = await askClaude({ usage,
      system: SYSTEM,
      user: `${bioBlock(channelBio)}Тема видео (YouTube Long): ${topic || "—"}\n\nТекущий хук (паттерн «${current.type || "—"}», название «${current.title || "—"}»):\n${current.text}\n\nПерепиши этот хук с учётом правки: «${instruction}». Это доработка понравившегося варианта: сохрани его паттерн, структуру и всё, чего правка не касается. Верни JSON с ОДНИМ элементом в "hooks".`,
      maxTokens: 1500,
    });
    return extractJson(text);
  }

  if (!topic) throw new Error("Не указана тема ролика");
  // Перегенерация: пользователь уже видел прошлые варианты и нажал кнопку,
  // потому что хочет ДРУГИЕ — без этого блока модель на одинаковой теме
  // стабильно сходится к тем же трём «лучшим» хукам.
  const previousBlock =
    Array.isArray(body.previous) && body.previous.length
      ? `\n\nПрошлые варианты хуков — пользователь их уже видел и отклонил, поэтому запрещено повторять их паттерны, углы, конкретные детали и формулировки:\n${body.previous
          .map((h, i) => `${i + 1}. [${h.type || "—"}] ${h.text}`)
          .join("\n")}\n\nВыбери три ДРУГИХ паттерна из списка правил и найди в теме другие конкретные детали.`
      : "";
  const text = await askClaude({ usage,
    system: SYSTEM,
    user: `${bioBlock(channelBio)}Тема видео (YouTube Long): ${topic}${previousBlock}\n\nСгенерируй 3 варианта хука.`,
    maxTokens: 3000,
  });
  return extractJson(text);
});
