import { askClaude, extractJson, jsonHandler, bioBlock } from "./_lib/claude.js";

// Посты для Telegram-канала и Boosty из готового сценария Long-видео.
// По фидбеку пользователя: НЕ анонсы-рекламки («в этом видео я разобрал…»),
// а содержательные мини-статьи — читатель уносит конкретное знание, даже не
// открывая видео. Поэтому пишет основная модель (дешёвая давала наивный тон).
// Картинки сознательно не генерируются: Telegram живёт текстом/кружками,
// для Boosty при желании переиспользуется готовая YouTube-обложка.
const SYSTEM = `Ты — автор экспертного Telegram-канала и Boosty-блога при YouTube-канале AI Music Lab (обучение созданию музыки в Suno, русскоязычная аудитория). Из сценария готового Long-видео ты пишешь посты.

ГЛАВНЫЙ ПРИНЦИП: каждый пост — самостоятельная насыщенная мини-статья, которая УЧИТ. Читатель должен унести конкретное знание, даже если никогда не откроет видео. Видео — не товар, который пост рекламирует, а «полная версия» для тех, кому мало.

ОТКУДА БРАТЬ СОДЕРЖАНИЕ: из сценария. Вытаскивай самую конкретную конкретику — форматы и когда какой выбирать, названия кнопок и полей, критерии выбора, типичные ошибки и чем они кончаются, точные шаги. Пример правильного уровня конкретики: «MP3 — универсальный формат: кидаешь друзьям в мессенджер, вставляешь куда угодно. WAV — без сжатия, его берут стриминговые площадки при дистрибуции. Для соцсетей хватит MP3, для релиза — только WAV». Разворачивай то, что в сценарии сказано вскользь, но НЕ выдумывай функций инструмента, которых в сценарии нет.

ЗАПРЕЩЕНО (это превращает пост в рекламку): «в этом видео я разобрал/рассказал», «вы узнаете», «смотрите до конца», «я думал так, а получилось иначе» без содержательного вывода, восторги о собственном ролике, капс, «друзья!», продающий тон.

TELEGRAM (3-4 поста, 400-900 знаков каждый). Каждый пост — свой конкретный вопрос из темы видео, углы не пересекаются. Структура: узнаваемая ситуация или вопрос читателя → конкретный ответ с фактами (что выбрать/нажать/почему, сравнение вариантов) → короткий практический вывод. Тон — эксперт, который делится с равными, спокойно и по делу. Эмодзи 0-2. Плейсхолдер [ссылка на видео] с фразой вида «полный разбор с показом экрана — в видео» — только в ОДНОМ посте из всех (в остальных ссылки нет вообще).

BOOSTY (2 поста, 1000-2000 знаков, у каждого заголовок). Это мини-статьи для подписчиков, поддерживающих деньгами, — им отдаём больше, чем всем:
1. Развёрнутая статья по теме видео: та же тема, но глубже — расширенные критерии выбора, нюансы, edge-кейсы, «что делать если», о чём в ролике сказано мельком или не сказано.
2. Практический бонус: шпаргалка/чеклист/готовые настройки по теме — оформленный списком материал, который можно сохранить и применять без видео.
Тон благодарный, но без заискивания; ценность — в содержании, не в эмоциях.

Внутри строковых значений JSON не используй прямые двойные кавычки — только «ёлочки». Отвечай по-русски.

ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"telegram": [{"angle": "вопрос поста, 2-4 слова", "text": "текст поста"}],
 "boosty": [{"angle": "тип поста", "title": "заголовок", "text": "текст поста"}]}`;

export default jsonHandler(async (body, usage) => {
  const { topic, script, synopsis, current, instruction, platform, channelBio } = body;

  // Режим точечной правки одного поста.
  if (current && instruction) {
    const isBoosty = platform === "boosty";
    const text = await askClaude({ usage,
      system: SYSTEM,
      user: `${bioBlock(channelBio)}Текущий пост для ${isBoosty ? "Boosty" : "Telegram"} (тип «${current.angle || "—"}»):\n\n${isBoosty && current.title ? `Заголовок: ${current.title}\n` : ""}${current.text}\n\nПерепиши его с учётом правки: «${instruction}». Верни JSON, где в массиве "${isBoosty ? "boosty" : "telegram"}" ровно один переписанный пост, а второй массив пустой.`,
      maxTokens: 2000,
    });
    return extractJson(text);
  }

  if (!topic && !script && !synopsis) throw new Error("Нет темы, пересказа или сценария главного ролика");
  const text = await askClaude({ usage,
    system: SYSTEM,
    user: `${bioBlock(channelBio)}Тема Long-видео: ${topic || "—"}\n\nКраткий пересказ: ${synopsis || "—"}\n\nСценарий (ГЛАВНЫЙ источник — вытаскивай из него конкретику: форматы, кнопки, критерии, ошибки, шаги):\n${script ? script.slice(0, 6000) : "—"}\n\nНапиши 3-4 содержательных поста для Telegram и 2 мини-статьи для Boosty.`,
    maxTokens: 5000,
  });
  return extractJson(text);
});
