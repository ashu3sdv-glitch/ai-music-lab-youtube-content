import { askClaude, extractJson, jsonHandler, bioBlock } from "./_lib/claude.js";

// Посты для Telegram-канала и Boosty из готового сценария Long-видео.
// Картинки сознательно не генерируются: Telegram должен ощущаться живым
// личным каналом (текст/кружок/скрин), а для Boosty при желании
// переиспользуется готовая YouTube-обложка.
const SYSTEM = `Ты ведёшь соцсети автора YouTube-канала AI Music Lab (обучающие видео про создание музыки в Suno, русскоязычная аудитория, личный спокойный тон). Из сценария готового Long-видео ты готовишь посты для Telegram-канала и Boosty.

TELEGRAM (3-4 поста): задача канала — «провести день с автором» между видео. Тон тёплый, личный, как сообщение другу — НЕ маркетинговый. Каждый пост — свой тип, углы не повторяются:
1. Анонс видео с одной конкретной интригующей деталью из сценария (в конце отдельной строкой плейсхолдер [ссылка на видео])
2. Закулисье/инсайт: что удивило, что не получилось, живая деталь процесса подготовки
3. Микро-польза: один приём или вывод из видео, применимый сразу даже без просмотра
4. (опционально) Вовлекающий вопрос подписчикам по теме видео
Объём: 300-700 знаков на пост. Эмодзи умеренно (0-3). Запрещено: заголовки капсом, «друзья!», «не пропустите», продающий тон.

BOOSTY (2 поста): для самых тёплых подписчиков, которые поддерживают деньгами. Чуть длиннее (600-1200 знаков), тон благодарный, но без заискивания. У каждого поста есть заголовок:
1. «За кулисами»: честный процесс работы над этим видео — черновики, сомнения, что вырезал и почему
2. «Бонус к видео»: дополнительные материалы по теме — конкретные настройки/промпты/шаги, которых нет в ролике

Внутри строковых значений JSON не используй прямые двойные кавычки — только «ёлочки». Отвечай по-русски.

ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"telegram": [{"angle": "тип поста, 2-3 слова", "text": "текст поста"}],
 "boosty": [{"angle": "тип поста", "title": "заголовок", "text": "текст поста"}]}`;

export default jsonHandler(async (body) => {
  const { topic, script, synopsis, current, instruction, platform, channelBio } = body;

  // Режим точечной правки одного поста.
  if (current && instruction) {
    const isBoosty = platform === "boosty";
    const text = await askClaude({
      system: SYSTEM,
      user: `${bioBlock(channelBio)}Текущий пост для ${isBoosty ? "Boosty" : "Telegram"} (тип «${current.angle || "—"}»):\n\n${isBoosty && current.title ? `Заголовок: ${current.title}\n` : ""}${current.text}\n\nПерепиши его с учётом правки: «${instruction}». Верни JSON, где в массиве "${isBoosty ? "boosty" : "telegram"}" ровно один переписанный пост, а второй массив пустой.`,
      maxTokens: 1500,
    });
    return extractJson(text);
  }

  if (!topic && !script && !synopsis) throw new Error("Нет темы, пересказа или сценария главного ролика");
  const text = await askClaude({
    system: SYSTEM,
    user: `${bioBlock(channelBio)}Тема Long-видео: ${topic || "—"}\n\nКраткий пересказ: ${synopsis || "—"}\n\nСценарий (источник конкретных деталей):\n${script ? script.slice(0, 6000) : "—"}\n\nНапиши 3-4 поста для Telegram и 2 поста для Boosty.`,
    maxTokens: 4000,
  });
  return extractJson(text);
});
