import { askClaude, extractJson, jsonHandler } from "./_lib/claude.js";
import { SKILLS } from "./_lib/skills.js";

const SYSTEM = `Ты создаёшь промпт для генерации обложки (image prompt) канала AI Music Lab. Твоя рабочая инструкция — скилл thumbnail-canva ниже. Следуй ей точно.

# Скилл: thumbnail-canva

${SKILLS.thumbnail_canva}

---
ВАЖНО ПРО ОБРЕЗКУ: у gpt-image-2 нет ровно нужного соотношения сторон. Картинка сначала генерируется в ближайшем доступном размере, затем обрезается по центру до точного формата — детали безопасной зоны для каждого формата указаны ниже. Не помещай текст, лица и другие важные детали композиции в зону, которая будет обрезана.

ВАЖНО ПРО СОДЕРЖАНИЕ: используй переданный контекст (краткий пересказ/сценарий/описание/текст поста), а не только голую тему. Обложка должна отражать конкретное содержание ролика — конкретные объекты, действия, детали из контекста — а не быть общей иллюстрацией по одному словосочетанию. Если контекста нет — придумай конкретную, не абстрактную сцену на основе темы.

ВАЖНО ПРО РАСПОЛОЖЕНИЕ ТЕКСТА: заголовочный текст на обложке размещай в верхней трети кадра (если тип композиции по скиллу не требует иного) — это фирменный стиль канала и он же безопаснее по краям обрезки. Не размещай крупный текст или ключевые детали в нижней правой части кадра (там в YouTube поверх миниатюры лежит плашка длительности видео). Явно укажи это расположение текста в самом промпте для OpenAI Images API (например "bold title text in the upper third of the frame"), не оставляй расположение на усмотрение модели.

---
ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"prompt": "готовый промпт на английском для OpenAI Images API: композиция, стиль, свет, эмоция, текст на обложке (если нужен — коротко и по-русски), с учётом указанного соотношения сторон и безопасной зоны"}`;

const FORMAT_NOTES = {
  "16:9":
    "YouTube Long thumbnail. Финальный кадр 1536×864 (16:9). Генерируется в 1536×1024, затем обрезается по центру — срезается ~8% высоты сверху и ~8% снизу. Держи заголовочный текст, лица и ключевые объекты в центральных ~84% по высоте, ничего важного ближе 8% к верхнему и нижнему краю.",
  "9:16":
    "YouTube Shorts cover. Финальный кадр 864×1536 (9:16). Генерируется в 1024×1536, затем обрезается по центру — срезается ~8% ширины слева и ~8% справа. Держи заголовочный текст и ключевой объект в центральных ~84% по ширине, ничего важного ближе 8% к левому и правому краю.",
  "1:1": "Community post image, квадрат 1:1 — генерируется точно в размер, обрезка не требуется, можно использовать весь кадр.",
};

const VARIANT_NOTE = {
  A: "Это ВАРИАНТ A для A/Б-теста обложек на YouTube. Выбери Тип A (Talking Head) из скилла, если контекст это позволяет — иначе любой тип, но зафиксируй чёткую композицию, к которой будет контрастировать вариант Б.",
  B: "Это ВАРИАНТ Б для A/Б-теста обложек на YouTube. Варианты в A/Б-тесте должны быть содержательно, а не косметически разными — выбери ДРУГОЙ тип композиции из скилла (Тип B или Тип C) и ДРУГУЮ цветовую схему, чем типично ожидался бы вариант A с той же темой. Не повторяй ту же сцену другими словами — предложи другой визуальный подход к той же теме.",
};

export default jsonHandler(async (body) => {
  const { topic, aspect = "16:9", context, variant } = body;
  if (!topic) throw new Error("Не указана тема для обложки");
  const trimmedContext = context ? context.slice(0, 4000) : "";
  const variantNote = VARIANT_NOTE[variant] ? `\n\n${VARIANT_NOTE[variant]}` : "";
  const text = await askClaude({
    system: SYSTEM,
    user: `Тема: ${topic}\n\nФормат: ${FORMAT_NOTES[aspect] || aspect}${trimmedContext ? `\n\nКонтекст (сценарий/описание/пост — используй конкретные детали из него):\n${trimmedContext}` : ""}${variantNote}\n\nСгенерируй промпт обложки.`,
    maxTokens: 2000,
  });
  return extractJson(text);
});
