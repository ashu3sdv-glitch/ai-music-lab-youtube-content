import { askClaude, extractJson, jsonHandler, bioBlock } from "./_lib/claude.js";

// Сжатие сохранённой идеи до одного предложения-действия + автокатегория.
// Вызывается в фоне при добавлении идеи в банк — полный текст остаётся,
// короткая суть становится заголовком карточки.
const SYSTEM = `Ты ведёшь банк идей автора YouTube-канала. Тебе дают сохранённую заметку (вывод из чужого видео или собственную мысль). Сожми её до ОДНОГО предложения-действия: конкретно, с глаголом, чтобы по нему можно было действовать без чтения полного текста. Не теряй специфику (цифры, названия, механику) ради краткости.

Категории (выбери одну):
- Канал — рост, стратегия, упаковка его YouTube-канала
- Контент — конкретная идея видео, формата, рубрики
- Бизнес — монетизация, продукты, продажи, аудитория как клиенты
- Личное — привычки, мышление, продуктивность, развитие

Внутри строковых значений JSON не используй прямые двойные кавычки — только «ёлочки». Отвечай по-русски.

ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"summary": "одно предложение-действие", "category": "Канал|Контент|Бизнес|Личное"}`;

export default jsonHandler(async (body, usage) => {
  const { text, channelBio } = body;
  if (!text) throw new Error("Нет текста идеи");
  const raw = await askClaude({ usage,
    system: SYSTEM,
    user: `${bioBlock(channelBio)}Заметка:\n${String(text).slice(0, 3000)}\n\nСожми и категоризируй.`,
    maxTokens: 300,
  });
  return extractJson(raw);
});
