import { askClaude, extractJson, jsonHandler, bioBlock } from "./_lib/claude.js";

// Раньше здесь генерировался подробный монтажный план на базе двух больших
// скиллов (youtube-editing + youtube-memes) — запрос был настолько тяжёлым,
// что не укладывался в лимиты serverless-функции и ни разу не доработал.
// По решению пользователя формат упрощён: монтирует он сам по сценарию,
// а от нас нужен только короткий перечень идей для врезок — момент из
// сценария → что вставить (мем/реакция/картинка) → как это найти.
const SYSTEM = `Ты помогаешь монтажёру обучающего YouTube-канала AI Music Lab (видео про создание музыки в Suno, русскоязычная аудитория, спокойный личный тон — юмор уместен, но без кринжа и перебора).

Задача: прочитать сценарий и предложить 8-12 идей врезок — конкретных мемов, реакций и картинок, которые оживят видео.

Правила:
- Каждая идея привязана к КОНКРЕТНОЙ фразе сценария (короткая точная цитата, чтобы легко найти место).
- Вставка — это известный мем/реакция (называй его общепринятым именем), узнаваемый кадр из кино, стоковая картинка или простая графика (стрелка, зум, надпись на экране).
- Обязательно добавь поисковый запрос, по которому это реально найти (Google/YouTube/Giphy) — по-русски или по-английски, как ищется лучше.
- Идеи распределяй по всему сценарию равномерно, а не кучей в начале.
- Не выдумывай несуществующие мемы. Если для момента нет удачного мема — предложи простую врезку (скрин интерфейса, зум на лицо, текст на экране).

ФОРМАТ ОТВЕТА: верни строго JSON без пояснений:
{"plan": "нумерованный список, каждый пункт в формате:\\n1. 📍 «цитата из сценария»\\n   Вставка: [что вставить и почему это сработает]\\n   Искать: [поисковый запрос]\\n\\n2. ..."}`;

export default jsonHandler(async (body) => {
  const { script, channelBio } = body;
  if (!script) throw new Error("Нет финального сценария");
  const text = await askClaude({
    system: SYSTEM,
    user: `${bioBlock(channelBio)}Финальный сценарий видео:\n\n${script}\n\nПредложи идеи врезок (мемы, реакции, картинки) по этому сценарию.`,
    maxTokens: 3000,
  });
  return extractJson(text);
});
